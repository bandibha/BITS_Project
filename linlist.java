import java.util.*;

public class linlist {
    public static void main(String[] args) {
        LinkedList<Integer> ll = new LinkedList<>();
        ll.add(99);
        System.out.println(ll);
        ll.addFirst(89);
        System.out.println(ll);
        ll.addLast(909);
        System.out.println(ll);
    }
}
